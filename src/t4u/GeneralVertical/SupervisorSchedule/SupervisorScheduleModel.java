package t4u.GeneralVertical.SupervisorSchedule;

public class SupervisorScheduleModel {

	
	private String supervisorName;
    private String hubName;
    private String hubCode;
    private String shiftStartTiming;
    private String shiftEndTiming;
    private String contactNumber;
    private int systemId;
    private int customerId;
	public String getSupervisorName() {
		return supervisorName;
	}
	public void setSupervisorName(String supervisorName) {
		this.supervisorName = supervisorName;
	}
	public String getHubName() {
		return hubName;
	}
	public void setHubName(String hubName) {
		this.hubName = hubName;
	}
	public String getHubCode() {
		return hubCode;
	}
	public void setHubCode(String hubCode) {
		this.hubCode = hubCode;
	}
	public String getShiftStartTiming() {
		return shiftStartTiming;
	}
	public void setShiftStartTiming(String shiftStartTiming) {
		this.shiftStartTiming = shiftStartTiming;
	}
	public String getShiftEndTiming() {
		return shiftEndTiming;
	}
	public void setShiftEndTiming(String shiftEndTiming) {
		this.shiftEndTiming = shiftEndTiming;
	}
	public String getContactNumber() {
		return contactNumber;
	}
	public void setContactNumber(String contactNumber) {
		this.contactNumber = contactNumber;
	}
	public int getSystemId() {
		return systemId;
	}
	public void setSystemId(int systemId) {
		this.systemId = systemId;
	}
	public int getCustomerId() {
		return customerId;
	}
	public void setCustomerId(int customerId) {
		this.customerId = customerId;
	}
    
    
}
