package t4u.beans;

public class DelayData {
	private String delayAttribution;
	private String delayCode;
	private String delayType;
	
	public String getDelayAttribution() {
		return delayAttribution;
	}
	public void setDelayAttribution(String delayAttribution) {
		this.delayAttribution = delayAttribution;
	}
	public String getDelayCode() {
		return delayCode;
	}
	public void setDelayCode(String delayCode) {
		this.delayCode = delayCode;
	}
	public String getDelayType() {
		return delayType;
	}
	public void setDelayType(String delayType) {
		this.delayType = delayType;
	}
	
}
