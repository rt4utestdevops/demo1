package t4u.distributionlogistics;

public class IndentVehiclesCount {
	
	public int indentId;
	public String node;
	public int totalDedicated;
	public int totalAdhoc;
	public int totalAssignedDedicated;
	public int totalAssignedAdhoc;
	
	public String getNode() {
		return node;
	}
	public void setNode(String node) {
		this.node = node;
	}
	public int getIndentId() {
		return indentId;
	}
	public void setIndentId(int indentId) {
		this.indentId = indentId;
	}
	public int getTotalDedicated() {
		return totalDedicated;
	}
	public void setTotalDedicated(int totalDedicated) {
		this.totalDedicated = totalDedicated;
	}
	public int getTotalAdhoc() {
		return totalAdhoc;
	}
	public void setTotalAdhoc(int totalAdhoc) {
		this.totalAdhoc = totalAdhoc;
	}
	public int getTotalAssignedDedicated() {
		return totalAssignedDedicated;
	}
	public void setTotalAssignedDedicated(int totalAssignedDedicated) {
		this.totalAssignedDedicated = totalAssignedDedicated;
	}
	public int getTotalAssignedAdhoc() {
		return totalAssignedAdhoc;
	}
	public void setTotalAssignedAdhoc(int totalAssignedAdhoc) {
		this.totalAssignedAdhoc = totalAssignedAdhoc;
	}
	
	
	
	

}
